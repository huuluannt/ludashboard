const toIconName = (value: string) => {
  return value
    .replace(/([a-z0-9])([A-Z])/g, '$1-$2')
    .replace(/([A-Z])([A-Z][a-z])/g, '$1-$2')
    .toLowerCase();
};

const iconMap = Object.fromEntries(
  Object.entries(lucideIconMap).map(([name, component]) => [toIconName(name), component]),
) as Record<string, ComponentType<LucideProps>>;

Object.assign(iconMap, {
  edit: Edit2,
  lightning: Zap,
  rna: DnaOff,
  trash: Trash2,
});

interface IconProps extends LucideProps {
  name: string;
}

export const availableIcons = Object.keys(iconMap).sort((a, b) => a.localeCompare(b));

export default function Icon({ name, ...props }: IconProps) {
  if (name.startsWith('data:image/')) {
    const { size = 16, className } = props;
    return (
      <img
        src={name}
        alt=""
        className={`object-contain ${className ?? ''}`}
        style={{ width: size, height: size }}
      />
    );
  }

  const IconComponent = iconMap[name];
  if (!IconComponent) {
    return <Package {...props} />;
  }
  return <IconComponent {...props} />;
}
